package group10.client.enums;

/**
 * Enumerator for card matches.
 * Denotes what kind of match is present between two cards.
 *
 * @author Alperen Caykus, Mustafa Ozan Alpay
 */
public enum MatchType {
    /**
     * No match
     */
    NO,
    /**
     * Regular match
     */
    REGULAR,
    /**
     * Pisti match
     */
    PISTI,
    /**
     * Double pisti match
     */
    DOUBLE_PISTI;

}
